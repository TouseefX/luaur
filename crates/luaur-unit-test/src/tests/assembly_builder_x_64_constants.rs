#[cfg(test)]
#[test]
fn assembly_builder_x_64_constants() {
    use luaur_code_gen::records::assembly_builder_x_64::AssemblyBuilderX64;
    use luaur_code_gen::records::register_x_64::RegisterX64 as R;

    // C++ `AssemblyBuilderX64Fixture::check` with both code and data expectations.
    fn check(f: impl FnOnce(&mut AssemblyBuilderX64), code: &[u8], data: &[u8]) {
        let mut build = AssemblyBuilderX64::assembly_builder_x_64_bool_i32(false, 0);
        f(&mut build);
        build.finalize();
        assert_eq!(&build.code[..], code, "code mismatch");
        assert_eq!(&build.data[..], data, "data mismatch");
    }

    check(
        |b| {
            b.xor_(R::rax.into(), R::rax.into());
            let c = b.i64(0x1234567887654321i64);
            b.add(R::rax.into(), c);
            let c = b.f32(1.0);
            b.vmovss_operand_x_64_operand_x_64(R::xmm2.into(), c);
            let c = b.f64(1.0);
            b.vmovsd_operand_x_64_operand_x_64(R::xmm3.into(), c);
            let c = b.f32x4(1.0, 2.0, 4.0, 8.0);
            b.vmovaps(R::xmm4.into(), c);
            let arr: [u8; 16] = *b"hello world!123\0";
            let c = b.bytes(arr.as_ptr() as *const core::ffi::c_void, 16, 8);
            b.vmovupd(R::xmm5.into(), c);
            let c = b.f64x2(5.0, 6.0);
            b.vmovapd(R::xmm5.into(), c);
            b.ret();
        },
        &[
            0x48, 0x33, 0xc0, 0x48, 0x03, 0x05, 0xee, 0xff, 0xff, 0xff, 0xc4, 0xe1, 0x7a, 0x10,
            0x15, 0xe1, 0xff, 0xff, 0xff, 0xc4, 0xe1, 0x7b, 0x10, 0x1d, 0xcc, 0xff, 0xff, 0xff,
            0xc4, 0xe1, 0x78, 0x28, 0x25, 0xab, 0xff, 0xff, 0xff, 0xc4, 0xe1, 0x79, 0x10, 0x2d,
            0x92, 0xff, 0xff, 0xff, 0xc4, 0xe1, 0x79, 0x28, 0x2d, 0x79, 0xff, 0xff, 0xff, 0xc3,
        ],
        &[
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x14, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x18, 0x40, 0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x20, 0x77, 0x6f, 0x72, 0x6c, 0x64, 0x21,
            0x31, 0x32, 0x33, 0x00, 0x00, 0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00,
            0x80, 0x40, 0x00, 0x00, 0x00, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x80, 0x3f, 0x21, 0x43, 0x65, 0x87, 0x78, 0x56, 0x34, 0x12,
        ],
    );
}
