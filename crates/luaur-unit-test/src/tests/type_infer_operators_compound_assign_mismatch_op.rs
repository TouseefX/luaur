#[cfg(test)]
#[test]
fn type_infer_operators_compound_assign_mismatch_op() {
    use crate::functions::type_error_data_ref::type_error_data_ref;
    use crate::records::fixture::Fixture;
    use alloc::string::String;
    use luaur_analysis::records::type_mismatch::TypeMismatch;
    use luaur_ast::records::location::Location;
    use luaur_ast::records::position::Position;

    let mut fixture = Fixture::fixture_bool(false);
    let result = fixture.check_string_optional_frontend_options(
        &String::from(
            r#"
        local s = 10
        s += true
    "#,
        ),
        None,
    );

    assert_eq!(1, result.errors.len(), "{:?}", result.errors);
    assert_eq!(
        Location {
            begin: Position {
                line: 2,
                column: 13
            },
            end: Position {
                line: 2,
                column: 17
            }
        },
        result.errors[0].location
    );
    let tm = type_error_data_ref::<TypeMismatch>(&result.errors[0]).expect("expected TypeMismatch");
    let number_type = fixture.get_builtins().numberType;
    let boolean_type = fixture.get_builtins().booleanType;
    assert_eq!(number_type, tm.wanted_type);
    assert_eq!(boolean_type, tm.given_type);
}
